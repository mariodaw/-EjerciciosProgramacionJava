package anidados;
public class tercero {
    public static void main(String[] args) {
        
        int n=1;
        int m=1;
        
        
        for ( int i=n; i<10 ; i++)
            {
                for ( int j=0; j<m && j<11 ; j++){
                    System.out.print(n);  
                }
            System.out.println("");   
            n++;
            m++;
            }
    }
}